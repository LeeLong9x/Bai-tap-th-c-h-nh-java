public class cauhoi {
    String ch,da,daA,daB,daC,daD;

    public cauhoi(String ch, String da, String daA, String daB, String daC, String daD) {
        this.ch = ch;
        this.da = da;
        this.daA = daA;
        this.daB = daB;
        this.daC = daC;
        this.daD = daD;
    }

   
    public String getDaA() {
        return daA;
    }

    public void setDaA(String daA) {
        this.daA = daA;
    }

    public String getDaB() {
        return daB;
    }

    public void setDaB(String daB) {
        this.daB = daB;
    }

    public String getDaC() {
        return daC;
    }

    public void setDaC(String daC) {
        this.daC = daC;
    }

    public String getDaD() {
        return daD;
    }

    public void setDaD(String daD) {
        this.daD = daD;
    }

    public String getCh() {
        return ch;
    }

    public void setCh(String ch) {
        this.ch = ch;
    }

    public String getDa() {
        return da;
    }

    public void setDa(String da) {
        this.da = da;
    }
    
}
